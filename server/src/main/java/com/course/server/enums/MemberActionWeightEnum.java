package com.course.server.enums;

public enum MemberActionWeightEnum {

    WATCH(1, "查看"),
    LIKE(5, "喜欢"),
    COLLECT(10, "收藏");

    private Integer code;

    private String desc;

    MemberActionWeightEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getDesc() {
        return desc;
    }
}
